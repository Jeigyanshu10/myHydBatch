package com.technoelevate.myHydBatch.languagefunadmentals;

public class DefaultValuesForInstanceVariables {
	int x;
	double d;
	boolean b;
	String s;
	char c;

	public static void main(String[] args) {
		DefaultValuesForInstanceVariables dv = new DefaultValuesForInstanceVariables();
		System.out.println(dv.x);
		System.out.println(dv.d);
		System.out.println(dv.b);
		System.out.println(dv.s);
		System.out.println(dv.c);
	}
}
